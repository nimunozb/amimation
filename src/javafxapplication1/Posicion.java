package javafxapplication1;

/**
 *
 * @author Estudiante
 */
public class Posicion {
    private int ejex;
    private int ejey;

    public Posicion() {
    }

    public Posicion(int ejex, int ejey) {
        this.ejex = ejex;
        this.ejey = ejey;
    }

    public int getEjex() {
        return ejex;
    }

    public void setEjex(int ejex) {
        this.ejex = ejex;
    }

    public int getEjey() {
        return ejey;
    }

    public void setEjey(int ejey) {
        this.ejey = ejey;
    }
    
    
}
